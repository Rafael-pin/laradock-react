    {
        $request->validate([
            'name'=>'required',
            'email'=>'required',
            'company_id'=>'required'
        ]);

        try {
            
            User::create($request->post());

            return response()->json([
                'message'=>'User Created Successfully!!'
            ]);
        }catch(\Exception $e) {
            \Log::error($e->getMessage());
            return response()->json([
                'message'=>'Something goes wrong while creating a user!!'
            ],500);
        }
    }

